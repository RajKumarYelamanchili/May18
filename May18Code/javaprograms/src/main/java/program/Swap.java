package program;

public class Swap {

	public static void main(String[] args) {
		int a=3;
		int b=2;
		int temp;
		 System.out.println("Before swapping ");
		 System.out.println("a= " + a + "   " +"b= " +b);
		 temp =a;
		 a=b;
		 b=temp;
		 
		 System.out.println("After swapping ");
		 System.out.println("a= " + a + "   " +"b= " +b);
	}

}
